export async function searchJobApplications(searchTerm = "", limit = 100) {
  await ensureJobApplicationsTable();

  // If no search term, return all applications
  if (!searchTerm || searchTerm.trim() === "") {
    const { rows } = await query(
      `
        SELECT
          id,
          TO_CHAR(application_date, 'YYYY-MM-DD') as application_date,
          company_name,
          profession_applied,
          job_title,
          industry_sector,
          source,
          job_link,
          recruiter_name,
          recruiter_contact,
          referral_name,
          application_status
        FROM job_applications
        ORDER BY id ASC
        LIMIT $1
      `,
      [limit]
    );
    return rows;
  }

  // Search across multiple fields (case-insensitive, partial match)
  const searchPattern = `%${searchTerm}%`;

  const { rows } = await query(
    `
      SELECT
        id,
        TO_CHAR(application_date, 'YYYY-MM-DD') as application_date,
        company_name,
        profession_applied,
        job_title,
        industry_sector,
        source,
        job_link,
        recruiter_name,
        recruiter_contact,
        referral_name,
        application_status
      FROM job_applications
      WHERE 
        LOWER(company_name) LIKE LOWER($1)
        OR LOWER(profession_applied) LIKE LOWER($1)
        OR LOWER(job_title) LIKE LOWER($1)
        OR LOWER(recruiter_name) LIKE LOWER($1)
        OR EXISTS (
          SELECT 1 FROM unnest(referral_name) AS ref 
          WHERE LOWER(ref) LIKE LOWER($1)
        )
      ORDER BY id ASC
      LIMIT $2
    `,
    [searchPattern, limit]
  );

  return rows;
}
