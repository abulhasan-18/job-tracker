export function parseJobApplicationFormData(formData) {
  // Get application_date from form (required from user)
  const applicationDate = parseRequiredDate(formData.get("application_date"), "Application Date");
  
  // Validate that the date is not in the future
  const today = new Date();
  const year = today.getFullYear();
  const month = String(today.getMonth() + 1).padStart(2, '0');
  const day = String(today.getDate()).padStart(2, '0');
  const todayStr = `${year}-${month}-${day}`;
  
  if (applicationDate > todayStr) {
    throw new Error("Application date cannot be in the future.");
  }

  // Parse referral names - can be JSON array or individual form fields
  let referralNames = [];
  
  // First, try to get the hidden JSON field (primary method)
  const referralJson = formData.get("referral_name");
  console.log("[DEBUG] referral_name hidden field:", referralJson);
  
  if (referralJson && referralJson.trim() !== "" && referralJson !== "[]") {
    try {
      if (referralJson.startsWith("[")) {
        const parsed = JSON.parse(referralJson);
        referralNames = Array.isArray(parsed) 
          ? parsed.filter((name) => normalizeText(name) !== null)
          : [];
        console.log("[DEBUG] parsed from hidden JSON:", referralNames);
      }
    } catch (err) {
      console.error("[DEBUG] Error parsing hidden JSON:", err);
    }
  }
  
  // If no referral names from hidden field, try individual form fields (fallback)
  if (referralNames.length === 0) {
    let i = 0;
    while (formData.has(`referral_name_${i}`)) {
      const name = normalizeText(formData.get(`referral_name_${i}`));
      console.log(`[DEBUG] referral_name_${i}:`, formData.get(`referral_name_${i}`), "->", name);
      if (name !== null) {
        referralNames.push(name);
      }
      i++;
    }
    if (i > 0) {
      console.log("[DEBUG] parsed from individual fields:", referralNames);
    }
  }

   // Return as array (or null if empty) for text[] PostgreSQL type
   const referralName = referralNames.length > 0 ? referralNames : null;
   console.log("[DEBUG] final referral_name value:", referralName);

   return {
     id: normalizeText(formData.get("id")),
     application_date: applicationDate,
     company_name: parseRequiredText(formData.get("company_name"), "Company name"),
     profession_applied: parseRequiredText(formData.get("profession_applied"), "Profession Applied"),
     job_title: parseRequiredText(formData.get("job_title"), "Job Title"),
     industry_sector: parseRequiredText(formData.get("industry_sector"), "Industry Sector"),
     source: parseRequiredText(formData.get("source"), "Source"),
     job_link: parseRequiredText(formData.get("job_link"), "Job Link"),
     recruiter_name: normalizeText(formData.get("recruiter_name")),
     recruiter_contact: normalizeText(formData.get("recruiter_contact")),
     referral_name: referralName,
     application_status: parseRequiredText(formData.get("application_status"), "Application Status"),
   };
 }

